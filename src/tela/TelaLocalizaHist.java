    private void bImprimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bImprimeActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tLocaliza.getModel();
        if (tLocaliza.getSelectedRow() != -1) {
            int codigo = (Integer) modelo.getValueAt(tLocaliza.getSelectedRow(), 0);
          
        TelaImprimir ti = new TelaImprimir();
        ti.setN(bd.localiza(codigo));
        ti.setVisible(true);
        this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Não há nenhuma venda selecionada", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_bImprimeActionPerformed

