    private void preencheTabela() {
DefaultTableModel modelo = (DefaultTableModel) tLocaliza.getModel();
int i = modelo.getRowCount();
while (i-- > 0) {
    modelo.removeRow(i);
}
List c = bd.pesquisa2(tFiltro.getText());
for (Iterator it = c.iterator(); it.hasNext();) {
  
    Notas a = (Notas) it.next();
modelo.addRow(new Object[]{a.getCodigo(), a.getNome(), a.getDescricao(), a.getQuantidade(), a.getPreco(),a.getMetodo(), a.getPagamento(), a.getVezes(),a.getNomeCliente(),a.getData()});

}
}
